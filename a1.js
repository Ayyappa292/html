var nresult;
var na=2;
var nb=3;
var soperator;
switch(soperator)
{
    case '+':
        nresult=na+nb;
        console.log("the addtion is ",nresult);
        break;
    case '-':
        nresult=na-nb;
        console.log("the difference is",nresult);
        break;
    default:
        console.log("please enter a valid operator")
}
